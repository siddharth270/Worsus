// ── No-repeat word deck ──────────────────────────────────────────────────────
// Tracks used words per category in localStorage. Cycles through all 100
// words before any word can repeat (like a shuffled deck of cards).

const DECK_KEY = 'worsus_deck'

function loadDecks() {
  try { return JSON.parse(localStorage.getItem(DECK_KEY) || '{}') }
  catch { return {} }
}

function saveDecks(decks) {
  localStorage.setItem(DECK_KEY, JSON.stringify(decks))
}

function shuffle(arr) {
  const a = [...arr]
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]]
  }
  return a
}

export function getRandomWord(category) {
  const allWords = CATEGORIES[category].words
  const decks    = loadDecks()

  // If deck for this category is empty or missing, refill + reshuffle
  if (!decks[category] || decks[category].length === 0) {
    decks[category] = shuffle(allWords)
  }

  const word = decks[category].pop()
  saveDecks(decks)
  return word
}

export function resetDeck(category) {
  const decks = loadDecks()
  delete decks[category]
  saveDecks(decks)
}